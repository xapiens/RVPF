package org.rvpf.pap;

/**
 * PAP (Process Automation Protocols).
 */
public interface PAP
{
    /** Bit param (point). */
    String BIT_PARAM = "Bit";

    /** Connection state attribute (point). */
    String CONNECTION_STATE_ATTRIBUTE = "CONNECTION_STATE";

    /** Inactive attribute (points and origins). */
    String INACTIVE_ATTRIBUTE = "INACTIVE";

    /** Index param (point). */
    String INDEX_PARAM = "Index";

    /** Listener properties. */
    String LISTENER_PROPERTIES = "listener";

    /** Offset param (point). */
    String OFFSET_PARAM = "Offset";

    /** Origin property. */
    String ORIGIN_PROPERTY = "origin";

    /** Protocol support class properties. */
    String PROTOCOL_SUPPORT_CLASS_PROPERTIES = "pap.support.class";
}

